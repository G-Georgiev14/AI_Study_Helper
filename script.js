// Enhanced Modal functionality with dynamic navigation
window.openPromptModal = function(subject) {
    const modal = document.getElementById('promptModal');
    const modalTitle = document.getElementById('modalTitle');
    const modalPrompts = document.getElementById('modalPrompts');
    
    const subjectData = promptData[subject];
    if (!subjectData) return;
    
    modalTitle.textContent = subjectData.title;
    
    // Create dynamic navigation
    const navHTML = subjectData.navigation.map(nav => 
        `<button class="nav-btn" onclick="scrollToSection('${nav.id}')">${nav.label}</button>`
    ).join('');
    
    // Group prompts by navigation sections (4 prompts per section)
    const sections = subjectData.navigation.map((nav, index) => {
        const startIdx = index * 4;
        const endIdx = startIdx + 4;
        const sectionPrompts = subjectData.prompts.slice(startIdx, endIdx);
        
        return `
            <div class="prompt-section" id="${nav.id}">
                <h3>${nav.label}</h3>
                ${sectionPrompts.map((prompt, promptIndex) => createPromptItem(prompt, startIdx + promptIndex, subject)).join('')}
            </div>
        `;
    }).join('');
    
    modalPrompts.innerHTML = sections;
    
    // Update navigation
    const modalNav = modal.querySelector('.modal-nav');
    if (modalNav) {
        modalNav.innerHTML = navHTML;
    }
    
    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';
    
    // Scroll to usage section
    const usageSection = document.getElementById('usage');
    if (usageSection) {
        usageSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
    setTimeout(() => {
        const usageSection = document.getElementById('usage');
        if (usageSection) {
            usageSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
    }, 100);
};

// Helper function to create prompt items
function createPromptItem(prompt, index, subject) {
    return `
        <div class="prompt-item">
            <div class="prompt-title">${prompt.title}</div>
            <div class="prompt-description">${prompt.description}</div>
            <div class="prompt-code">${prompt.code}</div>
            <button class="copy-btn" onclick="copyPrompt(${index}, '${subject}')">Копирай промпта</button>
        </div>
    `;
}

// Scroll to section function
window.scrollToSection = function(sectionId) {
    const section = document.getElementById(sectionId);
    if (section) {
        section.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
};

window.closePromptModal = function() {
    const modal = document.getElementById('promptModal');
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
    
    // Close mobile menu if open
    const modalNav = document.getElementById('modalNav');
    const menuToggle = document.querySelector('.mobile-menu-toggle');
    const overlay = document.querySelector('.mobile-menu-overlay');
    if (modalNav) {
        modalNav.classList.remove('active');
    }
    if (menuToggle) {
        menuToggle.classList.remove('active');
    }
    if (overlay) {
        overlay.classList.remove('active');
    }
};

// Toggle mobile menu
window.toggleMobileMenu = function() {
    const modalNav = document.getElementById('modalNav');
    const menuToggle = document.querySelector('.mobile-menu-toggle');
    const overlay = document.querySelector('.mobile-menu-overlay');
    
    if (modalNav && menuToggle && overlay) {
        modalNav.classList.toggle('active');
        menuToggle.classList.toggle('active');
        overlay.classList.toggle('active');
    }
};

window.copyPrompt = function(promptIndex, subject) {
    const prompt = promptData[subject].prompts[promptIndex];
    const textToCopy = `${prompt.title}\n\n${prompt.description}\n\n${prompt.code}`;
    
    navigator.clipboard.writeText(textToCopy).then(() => {
        event.target.textContent = 'Копирано!';
        event.target.classList.add('copied');
        
        setTimeout(() => {
            event.target.textContent = 'Копирай промпта';
            event.target.classList.remove('copied');
        }, 2000);
    }).catch(err => {
        console.error('Failed to copy text: ', err);
    });
};

// Close modal when clicking outside
window.onclick = function(event) {
    const modal = document.getElementById('promptModal');
    if (event.target === modal) {
        closePromptModal();
    }
};

// Close modal with Escape key
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closePromptModal();
    }
});

