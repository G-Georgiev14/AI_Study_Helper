// FAQ Collapsible Functionality
function setupFAQ() {
    const faqQuestions = document.querySelectorAll('.faq-question');
    
    faqQuestions.forEach(question => {
        question.addEventListener('click', function() {
            const faqItem = this.parentElement;
            const answer = faqItem.querySelector('.faq-answer');
            const arrow = this.querySelector('.faq-arrow');
            const isOpen = answer.style.maxHeight && answer.style.maxHeight !== '0px';
            
            // Close all other FAQs
            faqQuestions.forEach(otherQuestion => {
                if (otherQuestion !== question) {
                    const otherItem = otherQuestion.parentElement;
                    const otherAnswer = otherItem.querySelector('.faq-answer');
                    const otherArrow = otherQuestion.querySelector('.faq-arrow');
                    
                    otherAnswer.style.maxHeight = '0';
                    otherAnswer.style.padding = '0 1.5rem';
                    otherArrow.style.transform = 'rotate(0deg)';
                    otherQuestion.style.background = 'white';
                }
            });
            
            // Toggle current FAQ
            if (isOpen) {
                answer.style.maxHeight = '0';
                answer.style.padding = '0 1.5rem';
                arrow.style.transform = 'rotate(0deg)';
                question.style.background = 'white';
            } else {
                answer.style.maxHeight = answer.scrollHeight + 'px';
                answer.style.padding = '0 1.5rem 1.5rem 1.5rem';
                arrow.style.transform = 'rotate(180deg)';
                question.style.background = '#f8fafc';
            }
        });
        
        // Add hover effect
        question.addEventListener('mouseenter', function() {
            const answer = this.parentElement.querySelector('.faq-answer');
            if (!answer.style.maxHeight || answer.style.maxHeight === '0px') {
                this.style.background = '#f1f5f9';
            }
        });
        
        question.addEventListener('mouseleave', function() {
            const answer = this.parentElement.querySelector('.faq-answer');
            if (!answer.style.maxHeight || answer.style.maxHeight === '0px') {
                this.style.background = 'white';
            }
        });
    });
}
